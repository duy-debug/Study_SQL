--SQL (Structured Query Language) là ngôn ngữ lập trình được sử dụng để thao
--tác với dữ liệu trong cơ sở dữ liệu quan hệ.SQL được sử dụng để tạo,
--truy vấn, thay đổi và xóa dữ liệu trong cơ sở dữ liệu

--DML (Data Manipulation Language): Các câu lệnh DML được sử dụng để thao tác
--với dữ liệu trong cơ sở dữ liệu, bao gồm các câu lệnh chèn, cập nhật, xóa và truy vấn.
--DDL (Data Definition Language): Các câu lệnh DDL được sử dụng để định nghĩa cấu trúc của cơ sở dữ liệu,
--bao gồm các bảng, cột, chỉ mục và ràng buộc.
--DCL (Data Control Language): Các câu lệnh DCL được sử dụng để kiểm soát quyền truy cập vào cơ sở dữ liệu,
--bao gồm các câu lệnh cấp phép và thu hồi quyền.
--TCL (Transaction Control Language): Các câu lệnh TCL được sử dụng để quản lý các giao dịch trong cơ sở dữ liệu,
--bao gồm các câu lệnh bắt đầu, xác nhận và hoàn tác giao dịch.


--Các câu lệnh DML (Data Manipulation Language) được sử dụng để thao tác với dữ liệu trong cơ sở dữ liệu. Các câu lệnh DML thường được sử dụng để thêm, cập nhật, xóa và truy vấn dữ liệu.

--Cụ thể, các câu lệnh DML bao gồm:
--INSERT INTO: Dùng để thêm dữ liệu mới vào bảng.
--UPDATE: Dùng để cập nhật dữ liệu trong bảng.
--DELETE: Dùng để xóa dữ liệu trong bảng.
--SELECT: Dùng để truy vấn và lấy dữ liệu từ bảng.

--Cụ thể, các câu lệnh DDL bao gồm:
--CREATE TABLE: Tạo một bảng mới.
--ALTER TABLE: Thêm, xóa hoặc sửa đổi các cột trong bảng.
--DROP TABLE: Xóa một bảng.
--CREATE INDEX: Tạo một chỉ mục để tăng tốc độ truy vấn các cột.
--DROP INDEX: Xóa một chỉ mục.
--CREATE CONSTRAINT: Tạo một ràng buộc để đảm bảo tính toàn vẹn của dữ liệu.
--DROP CONSTRAINT: Xóa một ràng buộc.

--Cụ thể, các câu lệnh DCL bao gồm:
--GRANT: Cấp quyền truy cập cho người dùng.
--REVOKE: Thu hồi quyền truy cập cho người dùng.

--Cụ thể, các câu lệnh DCL bao gồm:
--BEGIN TRANSACTION: Bắt đầu một giao dịch.
--COMMIT TRANSACTION: Xác nhận một giao dịch.
--ROLLBACK TRANSACTION: Hoàn tác một giao dịch.

